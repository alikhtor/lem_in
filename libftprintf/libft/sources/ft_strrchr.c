char	*ft_strrchr(const char *s, int c)
{
	char *t;

	t = 0;
	while (*s)
	{
		if (*s == (char)c)
			t = (char*)s;
		s++;
	}
	if ((char)c == 0)
		return ((char*)s);
	return (t);
}
