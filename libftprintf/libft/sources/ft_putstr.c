#include "../includes/libft.h"

void	ft_putstr(char const *s)
{
	size_t	i;

	i = 0;
	if (s)
	{
		while (s[i] != 0)
		{
			ft_putchar(s[i]);
			i++;
		}
	}
	return ;
}
